import UIKit

extension UIColor {
    class var sunsetOrange: UIColor {
        return UIColor(red: 242/255, green: 71/255, blue: 63/255, alpha: 1)
    }
    
    class var blackRussian: UIColor {
        return UIColor(red: 29/255, green: 34/255, blue: 45/255, alpha: 1)
    }
}
